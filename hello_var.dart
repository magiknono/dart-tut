void main() {
  // implicit var
  var a = 1;
  print(a);

  // explicit var with type
  int b = 2;
  print(b);

  // constant var (cannot be change)
  final c = "hello";
  // c = "hello"; // = ERROR
  print(c);

  // constant var with keyword
  const d = "World"; // Number, STring, Boolean, Array, Map, Symbol
  print(d);

}